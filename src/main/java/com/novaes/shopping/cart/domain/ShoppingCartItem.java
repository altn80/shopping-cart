package com.novaes.shopping.cart.domain;

/**
 *
 * @author kbos
 */
public class ShoppingCartItem {

    private final int id;
    private final String name;
    private final double price;

    public ShoppingCartItem(int id, String name, double price) {
        this.id = id;
        this.name = name;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 31 * hash + this.id;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final ShoppingCartItem other = (ShoppingCartItem) obj;
        return this.id == other.id;
    }
    
    

}
